# Fonction CarteEtu
def carte_etu(nom, prenom, numetu):
    print("--- Carte étudiante ----")
    print("Nom :", nom)
    print("Prenom :", prenom)
    print("Numéro étudiant :", numetu)


carte_etu("Bourhani", "Dounya", 1234567)


def moyenne(n1, n2, n3, n4, n5):
    print("Votre moyenne est de :", (n1 + n2 + n3 + n4 + n5) / 5)


moyenne(1, 2, 3, 4, 5)
